<h3>Some parting wisdom</h3>
<p>Staying relevant and updated with everything in the digital industry is a never-ending battle. What makes it worth it is knowing the technologies you use are brand new and could be used in a way that completely re-shapes the web as we know it. So keep at it! </p>

