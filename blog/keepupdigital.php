<h2>Digital Industry: Staying relevant as a student</h2>
<span class='line'></span>

<h3>So, whats going on?</h3>
<p>Every now and then things change – it’s a given, a way of life but when those changes mean your reading product documentations every two minutes and constantly adapting your skills, is the digital world really all worth it? Well, of course! Who else is going to make websites that adhere to every WCAG guideline ever invented? As it stands there are double the staff aged between 31 and 41 in the digital industry than those between 18 and 24 (<a href='http://www.slideshare.net/ManchesterDigital/manchester-digital-skills-audit-2016-results' title='Digital Skills audit results'>Digital Skills Festival, 2016</a>). Personally this for me was a huge shock, as there’s whole companies dedicated to developing products for younger people and yet there’s a lack of those people within the industry. This brought to question my own obstacles with the industry. Maybe the dilemmas I get into now are discouraging younger people to be involved in the digital industry. Other than University, how do young people even break into the digital industry? On a personal note - my first break into the real world of digital work came from my Twitter page as I was lucky enough to be offered a placement at <a href='http://www.supremo.tv' title='Web development agency located in Manchester' class='default_link'>Supremo</a>. This I feel highlights the importance of having an active and professional social media presence - as you could have potential future employers scouting your profile at any time.  Despite my delight, I did find myself wondering if I was really ready. However I did take the opportunity to learn all of their tools, which in turn helped me understand how an agency functions on a day to day basis.  </p>

<h3>Problems faced</h3>
<p>One constant problem I face is trying to create something original. Scrolling through Dribbble or Behance for design ideas starts to feel more and more like cheating. <a href='https://www.youtube.com/watch?v=taAm6RT3t7I&feature=youtu.be' title='Mental health in tech talk.'>A talk by Chris Murphy</a> on Mental Health in tech touches on the term ‘Imposter Syndrome’. He gives a great analogy for this term that relates to me personally: “imagine you’re building a tower of cards and just waiting for someone to come over at any time and topple it.” In my case the tower represents a website I’m building and I’m waiting to be called out for my code.  Although none of my code is copied and I understand every line, there’s still a fear that I’m never going to be truly ready for industry. What doesn't help is that tools are forever changing. There are a thousand different ways to complete one task and every week a different way becomes industry standard.</p>
<figure>
<img src='http://imgs.xkcd.com/comics/git.png' alt='Xkcd comic about Github'>
<figcaption>xkcd comic illustrating the use of Git</figcaption>
</figure>
<h3>Fixing these problems</h3>
 <p>There is of course no quick medicine you can use. However, overcoming these problems requires you to take a step out of the role your currently in. Take a look at everything you have achieved during your project and compare it to past projects, analyse your progress and see how far you’ve come. It isn't until you look at the breakthroughs you've made that you realise how much you've learned!</p>


<p>A positive to come from all the blood, sweat and html is that I’m in one of the best places, there is a ratio 3:2 of males to females in the digital industry. Manchester Metropolitan University also has a 23% recruitment rate from all local universities.</p>

	<figure>
	<img src='../images/keepupdigital/digitalskillsreport.jpg' alt='Report showing University employment percentage'>
