<h3>Design</h3>
<p>The design phase for this website was relatively easy – a small website consisting of five or six unique pages. I used the research I had gathered on competitors to start formulating some paper drawn wireframes. Once I had a solid layout for each of my pages, I began to slowly generate a style guide. Using colours and fonts that I had picked up during my design trend analysis I created some simple guidelines to follow throughout the design phase. Then everything went digital! Starting with the wireframes, scanning up the low-fidelity versions to act as blueprints for my newer high-fidelity wireframes crafted using the Sketch app. Once the design of the site had been generalised I needed to start filling in some of the media content – This was where my trusty Canon 70D came into play, using photos I had either already taken or ones captured specifically for this website I started adding them to the design I had made in Sketch. Once I was happy with the media and the overall feel of the website design it was time to move onto the development.</p>
<figure>
<img src='../images/developmentprocess/fontpairs.jpg' alt='Omnes and lora font pairs comparison'>
